numero1 = int(input("Digite um número: "))

numero2 = int(input("Digite o segundo número: "))

if numero1==numero2: #operador de igualdade
    print("O número {} é igual a {}".format(numero1, numero2))

if numero1 != numero2: #operador de diferença
    print("O número {} é diferente de {}".format(numero1, numero2))

if numero1 < numero2: #operador menor que
    print("O número {} é menor que {}".format(numero1, numero2))

if numero1 > numero2: #operador maior que
    print("O número {} é maior que {}".format(numero1, numero2))

if numero1 >= numero2: #operador maior ou igual que
    print("O número {} é maior ou igual a {}".format(numero1,numero2))

if numero1 <= numero2: #operador menor ou igual que
    print("O número {} é menor ou igual a {}".format(numero1,numero2))



