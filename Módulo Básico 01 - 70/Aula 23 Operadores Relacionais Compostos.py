"""
Maior que: >
Menor que: <
Maior ou igual: >=
Menor ou igual: <=
"""

idade = int(input("Informe sua idade: "))

if idade <= 0:
    print("Sua idade não pode ser menor ou igual a zero ")

elif idade >= 150:
    print("Sua idade não pode ser superior a 150 anos! ")
elif idade < 18:
    print("Sua idade precisa ser maior que 18 anos")



